        <p>
            La Fundación Educacional Vygotsky, es una institución sin fines de LUCRO, se crea producto de la ley de Inclusión escolar N°20.845, viene a dar continuidad al Colegio Vygotsky, fundado un 24 de agosto del año 2000.
        </p>
        <p>
        Somos un establecimiento con reconocimiento oficial para los niveles de: Pre-Escolar, Educación Básica y Media Científico Humanista, todos con Jornada Escolar Completa, estos niveles bajo un sistema de alta exigencia escolar y que requiere la participación de los padres y familia de manera activa y constante, por considerar que son el  pilar fundamental en los logros de sus hijos e hijas.
</p>
        <p>
        Nuestro establecimiento trabaja bajo la modalidad de financiamiento compartido y libera de cualquier tipo de cobros a los alumnos que certifiquen la condición de alumnos Prioritarios.
</p>
        <p>
        Dentro de estos años asumimos el desafío de trabajar con niñas y niños con necesidades educativas especiales de carácter permanente bajo los diagnósticos certificados de Trastornos de la relación y comunicación y Discapacidad Intelectual leve. Es en estas necesidades educativas especiales que tenemos nuestra mayor experiencia. En Educación Parvularia NO contamos con experiencia para atender Trastorno Especifico del lenguaje. Nuestro PIE, incorpora alumnos y alumnas con Necesidades Educativas Especiales de carácter transitorio, desde el 2do. Año básico, considerando el 1ro. Básico como esencial para aportar la evidencia para su ingreso al programa.
        </p>
        <p>
        El objetivo de nuestro Proyecto Educativo Institucional es lograr una educación Integral e Inclusiva, que considera lo intelectual, lo ético,  lo  artístico, lo  estético,  lo cívico, lo social y  lo ambiental.  Para lograr este anhelado sueño tenemos 4 pilares que sostienen y dan vida al proceso y progresión de nuestros alumnos y alumnas, estos son: Familia, Disciplina, Esfuerzo  y Excelencia. Cada uno de ellos aporta hasta completar la realización y formación de las futuras generaciones de nuestro establecimiento y ciudadanos de nuestro país, en una visión global, cívica, responsable e integral del ser humano. 
        </p>
        <p>
        Dentro del Proyecto Educativo Institucional PEI, lo   fundamental de nuestro Plan de Mejoramiento Educativo PME es el mejoramiento continuo de los aprendizajes  de nuestros alumnos  y alumnas, este es, el compromiso de toda la comunidad escolar: Directivos, docentes, estudiantes, asistentes de la educación, padres  y apoderados.  Cada uno de ellos trabaja para que los resultados  y las prácticas  institucionales y pedagógicas  respondan a las necesidades educativas de nuestros  estudiantes. Todas  las   áreas de proceso (Gestión del Currículum, Liderazgo Escolar, Convivencia y Gestión de Recursos) son abordadas para  lograr el pleno cumplimiento de los principios esenciales de nuestro Proyecto, considerando incluso la utilización de más del 50% de los recursos de la ley N°20.248 SEP en la contratación de recursos humanos.
        </p>
        <p>
        Todas nuestras practicas  y  acciones  son coherentes  y consistentes con el  Curriculum,  la Visión,  La Misión y  Los Objetivos Estratégicos (formativos, pedagógicos, organizacionales, de  gestión, comunitarios  e  integración).
        </p>
        <p>
        Sobre el financiamiento, nuestro proyecto educativo, es un establecimiento de Financiamiento Compartido, el límite de cobro máximo, está regulado anualmente por Art N° 22 transitorio Ley 20.845.
        </p>
        <p>
        Dentro de nuestro alumnado y tal como lo establece el convenio de igualdad de oportunidades y excelencia educativa SEP, los alumnos(as) que cuenten con la condición de “Prioritarios”, están exentos de todo tipo de COBRO anual, mientras mantengan la condición.
        </p>
        <p> 
        Estos antecedentes son de suma importancia porque grafican una composición de familias y aspiraciones con una mixtura y compromiso social.        
        </p>
